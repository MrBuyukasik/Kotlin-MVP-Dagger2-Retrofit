    }

    private fun injectDependency() {
           val listComponent = DaggerFragmentComponent.builder()
               .fragmentModule(FragmentModule())
               .build()

           listComponent.inject(this)
    }

    private fun initView() {
        presenter.loadData()
    }

    companion object {
        val TAG: String = "ListFragment"
    }
}